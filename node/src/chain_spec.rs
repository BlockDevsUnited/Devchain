/// Configure initial storage state for FRAME modules.
fn testnet_genesis(
	wasm_binary: &[u8],
	initial_authorities: Vec<(AuraId, GrandpaId)>,
	root_key: AccountId,
	endowed_accounts: Vec<AccountId>,
	_enable_println: bool,
) -> GenesisConfig {
	// This is the simplest bytecode to revert without returning any data.
	// We will pre-deploy it under our precompile so they can be called from contracts.
	// (PUSH1 0x00 PUSH1 0x00 REVERT)
	let revert_bytecode = vec![0x60, 0x00, 0x60, 0x00, 0xFD];

	let num_endowed_accounts = endowed_accounts.len();

	GenesisConfig {
		system: SystemConfig {
			// Add Wasm runtime to storage.
			code: wasm_binary.to_vec(),
			changes_trie_config: Default::default(),
		},
		balances: BalancesConfig {
			// Configure endowed accounts with initial balance of 1 << 60.
			balances: endowed_accounts.iter().cloned().map(|k| (k, 1 << 60)).collect(),
		},
		aura: AuraConfig {
			authorities: initial_authorities.iter().map(|x| (x.0.clone())).collect(),
		},
		grandpa: GrandpaConfig {
			authorities: initial_authorities.iter().map(|x| (x.1.clone(), 1)).collect(),
		},
		sudo: SudoConfig {
			// Assign network admin rights.
			key: root_key,
		},
		council: Default::default(),
		treasury: Default::default(),
		society: SocietyConfig {
			members: endowed_accounts
				.iter()
				.take((num_endowed_accounts + 1) / 2)
				.cloned()
				.collect(),
			pot: 20000,
			max_members: 12,
		},
		evm: EVMConfig {
			accounts: {
				// Prefund the "Gerald" account
				let mut accounts = std::collections::BTreeMap::new();
				accounts.insert(
					H160::from_slice(&hex_literal::hex!("6Be02d1d3665660d22FF9624b7BE0551ee1Ac91b")),
					GenesisAccount{
						nonce: U256::zero(),
						// Using a larger number, so I can tell the accounts apart by balance.
						balance: U256::from(1u64 << 61),
						code: vec![],
						storage: std::collections::BTreeMap::new(),
					}
				);
				// Put some bogus revert code under our precompile contract so it can be called from Solidity
				accounts.insert(
					H160::from_slice(&hex_literal::hex!("0000000000000000000000000000000000000006")),
					GenesisAccount {
						nonce: Default::default(),
						balance: Default::default(),
						storage: Default::default(),
						code: revert_bytecode.clone(),
					},
				);
				accounts
			}
		},
		ethereum: EthereumConfig{},
	}
}
